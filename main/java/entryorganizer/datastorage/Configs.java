package entryorganizer.datastorage;

/**
 *
 * @author teopetuk89
 */
public class Configs {
    
    private String dataTypesPath;
    private String entityTypesPath;
    private String representationsPath;
    private String representationSetsPath;

    public String getDataTypesPath() {
        return dataTypesPath;
    }

    public void setDataTypesPath(String dataTypesPath) {
        this.dataTypesPath = dataTypesPath;
    }

    public String getEntityTypesPath() {
        return entityTypesPath;
    }

    public void setEntityTypesPath(String entityTypesPath) {
        this.entityTypesPath = entityTypesPath;
    }

    public String getRepresentationsPath() {
        return representationsPath;
    }

    public void setRepresentationsPath(String representationsPath) {
        this.representationsPath = representationsPath;
    }

    public String getRepresentationSetsPath() {
        return representationSetsPath;
    }

    public void setRepresentationSetsPath(String representationSetsPath) {
        this.representationSetsPath = representationSetsPath;
    }
    
}
